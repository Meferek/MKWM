    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $user = User::find($id);
        $user->name = $request->name;
        $user->surname = $request->surname;
        $user->phone = $request->phone;
        $user->initials = strtoupper(substr($request->name, 0, 1).substr($request->surname, 0, 1));

        $user->save();

        return redirect('/account')->with('success', 'Pomyślnie zmieniono dane');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $user = User::find($id);
        if($user->role == 10)
            return redirect()->back()->with('danger', 'Nie możesz usunąć administratora');
        else {
            $user->delete();
            return redirect()->back()->with('warning', 'Pomyślnie usunięto użytkownika');
        }
 
    }
